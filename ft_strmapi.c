#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char				*res;
	unsigned int		i;

	if (ft_strlen(s) <= 0)
		return (NULL);
	res = ft_calloc(ft_strlen(s) + 1, sizeof(char));
	i = 0;
	while (s[i] != '\0')
	{
		res[i] = f(i, s[i]);
		i++;
	}
	res[i] = '\0';
	free(f);
	return (res);
}
